package Oct24;

public class Lab004 {
    public static void main(String[] args) {

        //Escape sequence

        System.out.println("This is Escape Sequence for program");

        System.out.println("This is a backslash: \\");
        System.out.println("This is a single quote: \'");
        System.out.println("This is a double quote: \"");
        System.out.println("This is a tab: \t space");
        System.out.println("This is a back space:\btest");
        System.out.println("This is a new line: \n -->");
        System.out.println("This is a carriage return: \r test"); // Overwrites the current line

    }
}
